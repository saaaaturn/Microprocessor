;TAO XUNG CO F= 1HZ, DO RONG XUNG 50% => muc cao = muc thap = 0.5s = 500 000us
ORG 0000H
	MAIN:
	MOV TMOD, #01H	;SET CHE DO 1
	LOOP:
	MOV R1,#50		;LAP 50 LAN
	CPL P3.0 		;DAO GIA TRI BIT P3.0
	ACALL DELAY
	JMP LOOP
	DELAY:
	MOV TH0, #0D8H	;65536'2^16' - 10000s --> TIMER DEM 500 LAN--> TRAN'
	MOV TL0, #0F0H
	SETB TR0		;CHO TIMER HOAT DONG
	JNB TF0,$		;BAO TRAN
	CLR TR0
	CLR TF0
	DJNZ R1,DELAY	;NEU CHUA DU 50 LAN THI TIEP TUC
	;SAU 500us CO BAO TRAN HOAT DONG, RESET LAI QUA TRINH
	RET
END
